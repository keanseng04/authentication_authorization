Rails.application.routes.draw do

  # ==============================================================================================
  # Static Pages
  # ==============================================================================================
  root 'pages#home'

  # ==============================================================================================
  # Resources
  # ==============================================================================================
  resources :users, only: [:new, :create] do
    get :activate, on: :member
  end
  resources :password_resets, only: [:new, :create, :edit, :update]

end
